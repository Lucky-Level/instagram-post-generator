export interface TypographyComposition {
  id: string;
  name: string;
  category:
    | "modern"
    | "elegant"
    | "bold"
    | "playful"
    | "minimal"
    | "editorial"
    | "street"
    | "luxury";
  previewText: { headline: string; subtitle?: string; cta?: string };
  layers: Array<{
    role: "headline" | "subtitle" | "cta" | "decoration";
    type: "textbox";
    props: {
      text: string;
      left: number;
      top: number;
      originX: "left" | "center" | "right";
      originY: "top" | "center" | "bottom";
      width: number;
      fontSize: number;
      fontFamily: string;
      fontWeight: string;
      fill: string;
      textAlign: "left" | "center" | "right";
      charSpacing?: number;
      lineHeight?: number;
      opacity?: number;
      stroke?: string;
      strokeWidth?: number;
      shadow?: {
        color: string;
        blur: number;
        offsetX: number;
        offsetY: number;
      };
      fontStyle?: "normal" | "italic";
      textTransform?: "uppercase" | "none";
    };
  }>;
  overlays?: Array<{
    type: "rect" | "circle";
    props: Record<string, any>;
  }>;
}

