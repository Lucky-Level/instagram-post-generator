export const TYPOGRAPHY_COMPOSITIONS: TypographyComposition[] = [
  ...modern,
  ...elegant,
  ...bold,
  ...playful,
  ...minimal,
  ...editorial,
  ...street,
  ...luxury,
];

export function getCompositionsByCategory(
  category: TypographyComposition["category"]
): TypographyComposition[] {
  return TYPOGRAPHY_COMPOSITIONS.filter((c) => c.category === category);
}

export function getCompositionById(
  id: string
): TypographyComposition | undefined {
  return TYPOGRAPHY_COMPOSITIONS.find((c) => c.id === id);
}
